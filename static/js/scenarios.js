// 按场景名称/描述匹配图标(纯函数)
export function scnIcon(name, desc) {
  const s = (name + " " + (desc || "")).toLowerCase();
  if (/qa|code|问答|代码/.test(s))              return SCN_ICONS.code;
  if (/trouble|shoot|排查|问题|bug|fix/.test(s)) return SCN_ICONS.tool;
  if (/log|日志/.test(s))                       return SCN_ICONS.log;
  if (/db|sql|数据/.test(s))                    return SCN_ICONS.db;
  return SCN_ICONS.folder;
}
